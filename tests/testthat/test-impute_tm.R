                              25.02218,23.50001,21.78116,19.94077,18.05925,
                              16.21886,14.50001),
                tolerance = 0.00001)

   expect_equal(impute_tm(0:23),c(14.50001,12.97783,11.71885,10.7781,10.19667,10,10.54276,
                              12.10559,14.49998,17.43715,20.56281,23.49998,
                              25.89439,27.45723,28,27.80333,27.22191,26.28116,
                              25.02218,23.50001,21.78116,19.94077,18.05925,
                              16.21886),
                tolerance = 0.00001)

   # Test time input
   t_v <- seq(as.POSIXct("2023-07-07 01:00:00"),
              as.POSIXct("2023-07-08"),
              by = "hours")

   expect_equal(impute_tm(t_v),c(12.97783,11.71885,10.7781,10.19667,10,10.54276,
                              12.10559,14.49998,17.43715,20.56281,23.49998,
                              25.89439,27.45723,28,27.80333,27.22191,26.28116,
                              25.02218,23.50001,21.78116,19.94077,18.05925,
                              16.21886,14.50001),tolerance = 0.000001)

})
